package com.Project.carpool;

public class AttendModel {
	
	private String email;
	private String name;
	private String phone;
	private String apemail;
	private String apname;
	private String apphone;
	private String subject;
	private String content;
	private int no;
	private int count;
	
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getApemail() {
		return apemail;
	}
	public void setApemail(String apemail) {
		this.apemail = apemail;
	}
	public String getApname() {
		return apname;
	}
	public void setApname(String apname) {
		this.apname = apname;
	}
	public String getApphone() {
		return apphone;
	}
	public void setApphone(String apphone) {
		this.apphone = apphone;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
